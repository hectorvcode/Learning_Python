'''
To check the data type of certain data/variable, we use the type function
type()
'''
print('type(10)', type(10))

print('type(3.14)',type(3.14) )

print('type(1 + 3j)', type(1 + 3j))

print('type("Hector")', type('Hector'))

print('type([1, 2, 3])', type([1, 2, 3]))

print('type({"name":"Asab"})', type({'name':'Asab'}))

print('type((2, 3, 4, 5))', type((2, 3, 4, 5)))

print('type({9.8, 3.14, 2.7})', type({9.8, 3.14, 2.7}))
