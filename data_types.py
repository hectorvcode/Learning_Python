
# Data types

"""
Number
Integer: negative, zero and positive ie. -3, 0, 3
Float: decimal numbers ie. -3.5, 0.5, 0.0
Complex: numbers with real and imaginary parts ie. 1 + j. 2 + 4j
"""


"""
String
A collection of one or more characters under single or double quotes
ie.
'Hector'
'Finland'
'I love learning'
"""


"""
Booleans
A boolean data type is either a True or False value, T and F should be always uppercase
ie.
True
False
"""


"""
List
Python list is an ordered collection which allows to store different data types. A list is similar to an array in JavaScript
ie.
[0, 1, 2, 3, 4, 5]
['Banana', 'Orange', 'Mango', 'Apple']
['Banana', 10, False, 3.14]
"""


"""
Dictionary
A Python dictionary is an unordered collection of data in a key value pair format
ie.
{
    'first_name':'Hector',
    'last_name':'Vasquez',
    'country':'Colombia',
    'age':'125',
    'is_married':'True',
    'skills':['JS', 'React', 'Node', 'Python'],
}
"""


"""
Tuple
A tuple is an ordered collection of different data types like list but tuples can not be modified once they are created. They are immutable
ie.
['Hector', 'Paul', 'Brook', 'Lidia']
['Earth', 'Jupiter', 'Neptune', 'Mars']
"""


"""
Set
It's a collection of data types similar to list and tuple. Unlike list and tuple, set is not an ordered collection of items. Like in Maths, set in Python stores only unique items
ie.
{2, 4, 3, 5}
{3.14, 9.81, 2.7} order is not important in set
"""

